const PortfolioForm = (props) => {
  const [inputVal, setInputVal] = useState("");

  const handleStockInput = (e) => {
    setInputVal(e.target.value.toUpperCase());
  };

  class PortfolioStock {
    constructor(symbol, id) {
      this.symbol = symbol;
      this.id = id;
    }
  }

  const handleSubmit = (e) => {
    e.preventDefault();
    // console.log(inputVal);

    // THIS IS TO CHECK IF IT IS A VALID STOCK
    if (masterKeys.indexOf(inputVal) < 0) {
      alert("This is not a valid stock for Dead Cat. Please try again.");
      return;
    }
    var newPortfolioObj;
    const currentPortfolio = JSON.parse(
      localStorage.getItem("deadCatPortfolio")
    );
    // FIRST INSTANCE OF STORAGE ==============================================
    if (currentPortfolio === null) {
      // CREATE NEW PORTFOLIO OBJ
      const id = 1;
      newPortfolioObj = new PortfolioStock(inputVal, id);
      localStorage.setItem(
        "deadCatPortfolio",
        JSON.stringify([newPortfolioObj])
      );
      props.handleNewStorage()
      setInputVal("");
      return;
    }

    // ONE IN STORAGE ALREADY =================================

    if (currentPortfolio.length >= 10) {
      alert(
        "Only ten stock allowed in the portfolio. Please delete some and make room for new stocks."
      );
      return;
    }
    // CHECK FOR DUPLICATES -----------------------------------
    for (let i = 0; i < currentPortfolio.length; i++) {
      if (currentPortfolio[i].symbol == inputVal) {
        alert("This stock is already in your portfolio.");
        return;
      }
    }

    // CREATE NEW PORTFOLIO OBJ -----------------------------------
    var id = Number(currentPortfolio[0].id) + 1;
    newPortfolioObj = new PortfolioStock(inputVal, id);
    currentPortfolio.unshift(newPortfolioObj);

    localStorage.setItem("deadCatPortfolio", JSON.stringify(currentPortfolio));
    props.handleNewStorage()
    setInputVal("");
  };

  return (
    <div className="portfolio-form">
      <form onSubmit={(e) => handleSubmit(e)}>
        <input
          tabIndex={props.activePortfolio ? -1 : 0}
          minLength="1"
          maxLength="4"
          value={inputVal}
          onChange={(e) => handleStockInput(e)}
          type="text"
          placeholder="ABCD"
        />
        <input
          tabIndex={props.activePortfolio ? -1 : 0}
          type="submit"
          value="Add Stock"
        />
      </form>
    </div>
  );
};

export default PortfolioForm;
